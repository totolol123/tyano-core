--[[

You can specify a different spawn direction for each coordinate on the map.
This can also be done using spawn.xml, but most map editors don't support that feature and will just overwrite your directions there.

]]--

-- Examples:
-- spawn { x = 10, y = 20, z = 3, direction = NORTH }
-- spawn { x = 11, y = 21, z = 4, direction = SOUTH }
-- spawn { x = 12, y = 22, z = 5, direction = EAST }
-- spawn { x = 13, y = 23, z = 6, direction = WEST }
